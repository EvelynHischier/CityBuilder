	<h1>But du jeu</h1>
	<p>Le but du jeu Bâtisseur de cité est de faire prospérer une petite
		société dans la vallée du Nil pendant l’Égypte antique. Vous incarnez
		le dirigeant de cette communauté et vous allez prendre des décisions
		qui vont influencer le développement et l’avenir de votre peuple.</p>
	<h1>Le choix de l’emplacement de départ</h1>
	<p>Après avoir vécu en nomade plusieurs siècles, vous trouvez une terre
		cultivable et décidez de vous y installer. Certaines régions
		renferment cependant plus de terres exploitables que d’autres. La
		proximité de l’eau augmente par exemple les récoltes et permet de
		développer plus rapidement la population.</p>
	<h1>La gestion de la cité</h1>
	<p>Fonder une cité dans un emplacement approprié n’était que la
		première étape. Maintenant il vous faut donner des tâches à vos
		habitants pour assurer le bon fonctionnement de votre cité. À vous de
		répartir judicieusement votre population dans les différents rôles
		nécessaires à votre société.</p>
	<h1>Le pouvoir royal</h1>
	<p>Pour l’instant votre pouvoir est absolu car vous êtes seul aux
		commandes de votre cité. Mais un second dirigeant pourrait provoquer
		de l’instabilité dans la société.</p>
	<h1>La religion</h1>
	<p>Dans la pensée égyptienne, les dieux façonnèrent la Terre et
		établirent un ordre harmonieux permettant au miracle de la vie de
		s'accomplir jour après jour. C'est au seul pharaon, descendant des
		dieux, que revient la tâche d'assurer la pérennité de cette harmonie
		et de combattre les forces du mal L'entretien de l'harmonie divine
		exigeant de nombreux cultes. Une poignée de prêtres viennent en aide
		au pharaon dans cette quête.</p>
	<h1>L’administration</h1>
	<p>Pour pouvoir répartir efficacement les ressources produites, vous
		avez besoin d’un petit nombre de scribes qui pour vous sillonnent la
		région et comptabilisent les récoltes. Sans scribes, une grande partie
		de la nourriture pourrait être mal répartie dans votre cité et
		finalement perdue.</p>
	<h1>La guerre</h1>
	<p>Pour protéger vos habitants et vos récoltes, vous devez entretenir
		une bonne garnison de guerriers qui repousseront toutes incursions
		étrangères. Un nombre insuffisant de défenseur et les pillards
		s’empareront d’une partie de vos habitants pour en faire des esclaves.
	</p>
	<h1>La nourriture</h1>
	<p>Votre peuple a faim, nourrissez-le et il continuera de vous soutenir
		en tant que pharaon. Une récolte insuffisante, au contraire,
		provoquera une famine et fera diminuer votre population. Durant
		l’antiquité un habitant sur dix est généralement un paysan.</p>
	<h1>La servitude</h1>
	<p>Dans l’Égypte antique, les familles les plus riches peuvent acheter
		la vie de certains individus, souvent d’anciens prisonniers de guerre
		ou leurs descendants, pour augmenter leur confort de vie. Si ces
		puissantes familles estiment que vous ne les privilégiez pas en leur
		permettant d’avoir quelques domestiques, alors ils risquent de
		comploter contre vous.</p>
	<h1>Le choix d’une technologie</h1>
	<p>Pendant les années où votre société se développe, certains de vos
		habitants vont faire de nouvelles découvertes pour rendre la vie de la
		cité plus facile.</p>
	<h1>La poterie</h1>
	<p>Bien qu’il soit vraiment très utile d’avoir des vases et autres
		récipients pour contenir des denrées, cette technologie n’améliore pas
		la gestion globale de la cité mais offre un bonus à vos marchands et
		artisans.</p>
	<h1>L’écriture</h1>
	<p>Découverte incontournable, elle permet l’organisation de la vie en
		société et l’apparition du scribe qui permettra d’optimiser
		efficacement les récoltes et donc d’augmenter considérablement la
		quantité de nourriture produite.</p>
	<h1>Le grenier</h1>
	<p>Le grenier permet un stockage des ressources alimentaires plus
		efficace. Il apporte donc un bonus aux paysans.</p>
	<h1>L’écran des scores</h1>
	<p>Ici vous pouvez réaliser si vous avez créé une société prospère et
		puissante. N’hésitez pas à recommencer une partie pour tenter
		d’améliorer votre score en prenant de nouvelles décisions.</p>
